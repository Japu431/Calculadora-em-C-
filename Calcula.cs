namespace Calculadora
{
    public class Calcula
    {
        public decimal CalcSoma(decimal n1, decimal n2)
        {
            return n1 + n2;
        }

        public decimal CalcSubtracao(decimal n1, decimal n2)
        {
            return n1 - n2;
        }

        public decimal CalcMultiplicao(decimal n1, decimal n2)
        {
            return n1 * n2;
        }

        public decimal CalcDiv(decimal n1, decimal n2)
        {
            return n1 / n2;
        }
    }
}